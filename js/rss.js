function addMarker(location,source, link, title, description) {
	var image = 'img/BlackMarker.png';
	console.log(location);
	if ( location.lng < -30 ) 
	{
		if ( location.lat > 0 ) 
		{
			//North America
			image = 'img/LightGreyMarker.png';
		} 
		else 
		{
			//South America
			image = 'img/GreyMarker.png';
		}
	}
	else if ( location.lng < 60 )
	{
		if ( location.lat > 37 )
		{
			//Europe
			image = 'img/PaleMarker.png';
		} 
		else 
		{
			//Africa
			image = 'img/RedMarker.png';
		}
	} 
	else if ( location.lat > -10 ) 
	{
		//Australia
		image = 'img/PinkMarker.png';
	} 
	else 
	{
		//Asia
		image = 'img/MauveMarker.png';
	}
	
	var marker = new google.maps.Marker({
        position: location,
        map: map,
		icon: image
    });
    markers.push(marker);               
    var infowindow = new google.maps.InfoWindow({
        content: "<a href='" + link + "'>" + title + "</a>"
    });


    marker.addListener('click', function () {
        if(infoGlobal=== null){
            infoGlobal=infowindow;
        }
        else{
            infoGlobal.close();
            infoGlobal=infowindow;
        }

        infowindow.open(map, marker);
    });
	
	
	
}

            // Sets the map on all markers in the array.
            function setMapOnAll(map) {
                for (var i = 0; i < markers.length; i++) {
                    markers[i].setMap(map);
                }
            }

                // Removes the markers from the map, but keeps them in the array.
                function clearMarkers() {
                    setMapOnAll(null);
                }

                // Deletes all markers in the array by removing references to them.
                function deleteMarkers() {
                    clearMarkers();
                    markers = [];
                }

                // Shows any markers currently in the array.
                function showMarkers() {
                    setMapOnAll(map);
                }


